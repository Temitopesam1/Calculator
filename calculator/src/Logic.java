public class Logic {
    public static double add(double inputA, double inputB){
        return inputA + inputB;
    }

    public static double sub(double inputA, double inputB){
        return inputA - inputB;
    }

    public static double mul(double inputA, double inputB){
        return inputA * inputB;
    }

    public static double div(double inputA, double inputB){
        return inputA / inputB;
    }

    public static double modulo(double inputA, double inputB){
        return inputA % inputB;
    }
}
